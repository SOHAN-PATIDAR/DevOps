"Program to add, substract and multiply two numbers."

def add(n1,n2):
    """Add two integers"""
    return(n1+n2)

def substract(n1,n2):
    """Substract two integers"""
    return(n1-n2)

def multiply(n1,n2):
    """Multiply two integers"""
    return(n1*n2)

print(add(1,2))
print(substract(3,6))
print(multiply(4,5))
